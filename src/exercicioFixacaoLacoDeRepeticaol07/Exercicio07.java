package exercicioFixacaoLacoDeRepeticaol07;
/*7.	Crie um programa que imprima 11 vezes a frase " Hello World!" utilizando uma estrutura sequencial
        e uma estrutura de repetição while.*/
public class Exercicio07 {
    public static void main(String[] args) {
        Integer contador = 0;
        while (contador < 11) {
            System.out.println("Hello World!");
            contador++;
        }
    }

}
